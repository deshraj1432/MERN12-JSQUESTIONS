9// question 42. Print Weekday using Switch
// solution 
let month = +prompt("enter number to get month ")
switch (month) {
    case 1:
        console.log("January");
        
        break;
    case 2:
            console.log("February");
        break ;
    case 3:
        console.log("March");
        break;
    case 4:
        console.log(" April ")  
        break;
    case 5:
        console.log("May");
        break;
    case 6: 
    console.log(" June");
        break;      
    default:
           console.log("Invalid month")
        break;
}