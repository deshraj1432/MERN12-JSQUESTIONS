// question 11- Find circumference and area of circle
// solution
let  radius = +prompt(" Enter radius of circle");
let  c = 2 * Math.PI * radius;
let  a = Math.PI * radius * radius;
//Pi. This is the ratio of the circumference of a circle to its diameter.

console.log(` Circumference of circle is ${c} and area of circle is ${a}`);
